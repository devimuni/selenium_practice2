package accessmodifiers;

public class Accessmodifiers {
	//public access
	public int a=10;
	public void publicprint()

	{
		System.out.println("print value a  "+ a);

	}

	//protected access
	protected int b=20;
	protected void protectedprint()

	{
		System.out.println("print value b  "+ b);

	}
	//default access
	int c=30;
	void defaultprint()

	{
		System.out.println("print value c  "+ c);

	}
	
	//private access
	private	int d=40;
	private	void privateprint()

		{
			System.out.println("print value d  "+ d);

		}


}
