package javavariables;

public class primitivedatatypesdefaultvalues {

	//print default values of primitive data types
	
		byte bytevariable;
		short shortvariable;
		int intvariable;
		long longvariable;
		float floatvariable;
		double doublevariable;
		boolean booleanvariable;
		char charvariable;
		
	
	public static void main(String[] args) 
	{
		primitivedatatypesdefaultvalues obj = new primitivedatatypesdefaultvalues();
		System.out.println("Default byte variable values: " + obj.bytevariable);
		System.out.println("Default short variable values: " + obj.shortvariable);
		System.out.println("Default int variable values: " + obj.intvariable);
		System.out.println("Default long variable values: " + obj.longvariable);
		System.out.println("Default float variable values: " + obj.floatvariable);
		System.out.println("Default double variable values: " + obj.doublevariable);
		System.out.println("Default boolean variable values: " + obj.booleanvariable);
		System.out.println("Default char variable values: " + obj.charvariable);
	
	}

}
